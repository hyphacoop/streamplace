/**
 * Recursively find all .ftl files in a directory
 */
function findFtlFiles(dir) {
  const files = [];
  const entries = fs.readdirSync(dir, { withFileTypes: true });

  for (const entry of entries) {
    const fullPath = path.join(dir, entry.name);
    if (entry.isDirectory()) {
      files.push(...findFtlFiles(fullPath));
    } else if (entry.isFile() && entry.name.endsWith(".ftl")) {
      files.push(fullPath);
    }
  }

  return files;
}

/**
 * Read and combine all .ftl files for a locale
 */
function combineLocaleFiles(localeDir) {
  const ftlFiles = findFtlFiles(localeDir);

  if (ftlFiles.length === 0) {
    console.warn(`⚠️  No .ftl files found in ${localeDir}`);
    return "";
  }

  const contents = [];

  for (const filePath of ftlFiles) {
    try {
      const content = fs.readFileSync(filePath, "utf-8");
      const relativePath = path.relative(localeDir, filePath);

      contents.push(`# === ${relativePath} ===`);
      contents.push(content.trim());
      contents.push(""); // Empty line separator
    } catch (error) {
      console.error(`❌ Error reading ${filePath}:`, error.message);
    }
  }

  return contents.join("\n");
}

/**
 * Generate a JavaScript module that exports the translation content
 */
function generateJsModule(content) {
  // Escape backticks and ${} for template literal safety
  const escapedContent = content.replace(/`/g, "\\`").replace(/\$\{/g, "\\${");

  return `// Auto-generated by compile-translations.js
// Do not edit this file manually - it will be overwritten

export const messages = \`${escapedContent}\`;

// Export for CommonJS compatibility
if (typeof module !== 'undefined' && module.exports) {
  module.exports = { messages };
}
`;
}

/**
 * Generate Metro-compatible index.ts file
 */
function generateIndexFile(locales) {
  const camelCaseNames = locales.map((locale) => {
    return locale.replace(/-/g, "").toLowerCase() + "Messages";
  });

  const imports = locales
    .map((locale, index) => {
      const varName = camelCaseNames[index];

      // Fallback texts in various languages
      // Use simple English fallback for all locales
      const fallbackContent =
        "# Fallback\\nloading = Loading...\\nerror = Error\\ncancel = Cancel\\nsettings-title = Settings";

      return `try {
  // Try to import compiled translation modules
  ${varName} = require("./data/${locale}/messages.js");
} catch (error) {
  console.warn("[locale-mapping] Failed to load ${locale} translations:", error);
  ${varName} = {
    messages: ${JSON.stringify(fallbackContent)},
  };
}`;
    })
    .join("\n\n");

  const varDeclarations = camelCaseNames
    .map((name) => `let ${name}: { messages: string };`)
    .join("\n");

  const exportObj = locales
    .map((locale, index) => `  "${locale}": ${camelCaseNames[index]},`)
    .join("\n");

  const individualExports = camelCaseNames.join(", ");
  const supportedLocalesArray = locales.map((l) => `"${l}"`).join(", ");

  return `// Auto-generated by compile-translations.js
// Do not edit this file manually - it will be overwritten

${varDeclarations}

${imports}

// Export locale mapping for metro-loader
export const localeMessages = {
${exportObj}
} as const;

export { ${individualExports} };

export const supportedLocales = [${supportedLocalesArray}] as const;
export type SupportedLocale = (typeof supportedLocales)[number];

export function validateLocaleModule(
  locale: string,
  module: any,
): module is { messages: string } {
  return (
    module &&
    typeof module === "object" &&
    "messages" in module &&
    typeof module.messages === "string" &&
    module.messages.length > 0
  );
}

export function getAvailableLocales(): SupportedLocale[] {
  return supportedLocales.filter((locale) => {
    const module = localeMessages[locale];
    return validateLocaleModule(locale, module);
  });
}
`;
}

