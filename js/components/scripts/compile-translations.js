/**
 * Generate translations.ts utility file
 */
function generateTranslationsFile(locales) {
  const localeUnion = locales.map((l) => `"${l}"`).join(" | ");

  return `// Auto-generated by compile-translations.js
// Do not edit this file manually - it will be overwritten

import type { TranslationSource } from "../direct-provider";
import {
  dynamicActivate,
  loadAllLocales,
  loadLocale,
} from "../loaders/metro-loader";

/**
 * Load all translations dynamically from compiled .ftl files
 *
 * Usage:
 * \`\`\`tsx
 * import { loadDynamicTranslations } from './locales/translations';
 *
 * const App = () => (
 *   <DirectI18nProvider
 *     enableDynamicLoading={true}
 *     preloadAll={false} // Only load current locale
 *     debug={true}
 *   >
 *     <YourApp />
 *   </DirectI18nProvider>
 * );
 * \`\`\`
 */
export async function loadDynamicTranslations(
  debug = false,
): Promise<TranslationSource[]> {
  return loadAllLocales(debug);
}

/**
 * Load a specific locale dynamically
 * Useful for lazy loading when switching languages
 */
export async function loadDynamicLocale(
  locale: ${localeUnion},
  debug = false,
) {
  return loadLocale(locale, debug);
}

/**
 * Activate a locale dynamically (similar to Lingui's approach)
 * This loads and activates a locale in one step
 */
export async function activateLocale(
  locale: ${localeUnion},
  debug = false,
) {
  return dynamicActivate(locale, undefined, debug);
}
`;
}

/**
 * Main compilation function
 */
function compileTranslations() {
  console.log("🌍 Compiling translation files...");

  if (!fs.existsSync(LOCALES_DIR)) {
    console.error(`❌ Locales directory not found: ${LOCALES_DIR}`);
    process.exit(1);
  }

  // Get supported locales from manifest, but only include those with actual data directories
  const manifestLocales = manifest.supportedLocales;
  const locales = manifestLocales.filter((locale) => {
    const localeDir = path.join(LOCALES_DIR, locale);
    return fs.existsSync(localeDir) && fs.statSync(localeDir).isDirectory();
  });

  if (locales.length === 0) {
    console.error(`❌ No locale directories found in ${LOCALES_DIR}`);
    process.exit(1);
  }

  let totalFiles = 0;

  // Process each locale
  for (const locale of locales) {
    const localeDir = path.join(LOCALES_DIR, locale);
    const outputPath = path.join(localeDir, OUTPUT_FILENAME);

    console.log(`📦 Processing locale: ${locale}`);

    // Combine all .ftl files for this locale
    const combinedContent = combineLocaleFiles(localeDir);

    if (!combinedContent.trim()) {
      console.warn(`⚠️  Skipping ${locale} - no content found`);
      continue;
    }

    // Generate JavaScript module
    const jsContent = generateJsModule(combinedContent);

    // Write the output file
    try {
      fs.writeFileSync(outputPath, jsContent, "utf-8");
      console.log(`✅ Generated: ${path.relative(process.cwd(), outputPath)}`);
      totalFiles++;
    } catch (error) {
      console.error(`❌ Error writing ${outputPath}:`, error.message);
    }
  }

  // Generate Metro-compatible index.ts file
  const indexPath = path.join(LOCALES_BASE_DIR, INDEX_FILENAME);
  const indexContent = generateIndexFile(locales);

  try {
    fs.writeFileSync(indexPath, indexContent, "utf-8");
    console.log(`✅ Generated: ${path.relative(process.cwd(), indexPath)}`);
    totalFiles++;
  } catch (error) {
    console.error(`❌ Error writing ${indexPath}:`, error.message);
  }

  // Generate translations.ts utility file
  const translationsPath = path.join(LOCALES_BASE_DIR, TRANSLATIONS_FILENAME);
  const translationsContent = generateTranslationsFile(locales);

  try {
    fs.writeFileSync(translationsPath, translationsContent, "utf-8");
    console.log(
      `✅ Generated: ${path.relative(process.cwd(), translationsPath)}`,
    );
    totalFiles++;
  } catch (error) {
    console.error(`❌ Error writing ${translationsPath}:`, error.message);
  }

  // Generate language configuration file
  const languageConfigPath = path.join(
    LOCALES_BASE_DIR,
    LANGUAGE_CONFIG_FILENAME,
  );
  const languageConfigContent = generateLanguageConfigFile(locales);

  try {
    fs.writeFileSync(languageConfigPath, languageConfigContent, "utf-8");
    console.log(
      `✅ Generated: ${path.relative(process.cwd(), languageConfigPath)}`,
    );
    totalFiles++;
  } catch (error) {
    console.error(`❌ Error writing ${languageConfigPath}:`, error.message);
  }
  console.log(`🎉 Compilation complete! ${totalFiles} files generated.`);
}

// Run the compilation
compileTranslations();
